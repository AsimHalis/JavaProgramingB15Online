package day19;

public class CharAndForLoop {
    public static void main(String[] args) {

        for (char IChar = 'A'; IChar <= 'Z'; IChar++) {
            System.out.print(IChar + " ");
        }

        System.out.println();

        for (char kChar ='Z'; kChar >='A'; kChar--) {
            System.out.print(kChar+" ");
        }


        int star3='a';
        System.out.println( star3);

    }
}