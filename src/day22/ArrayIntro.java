package day22;

public class ArrayIntro {
    public static void main(String[] args) {
        byte[] number = new byte[4];
        number[0] = 12;
        number[1] = 23;
        number[2] = 40;
        number[3] = 67;
        System.out.println(number[0]);
        System.out.println(number[1]);
        System.out.println(number[2]);
        System.out.println(number[3]);
}
}
