package day07;

public class RelationOperator {
    public static void main(String[] args) {
        //<,>,<=,>=,==,!=,==>Relational operator
        System.out.println(7>5);//true
        System.out.println(9<5);//false
        System.out.println(17>=25);//false
        System.out.println(9<=9);//true
        System.out.println(6==5);//== means equal==>false
        System.out.println(6!=6);//!= means not equal.==>false
    }
}
