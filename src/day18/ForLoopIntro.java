package day18;

public class ForLoopIntro {
    public static void main(String[] args) {

        //count 1 to 5
        //for loop ()has 3 parts separated by:
        //first part:initialization
        //second part: termination condition
        //third part: increment or decrement to change condition
        //it will run after each iteration
        for(int i=1; i<=5; i++  ){//++i is same
            System.out.println("Happy Birthday "+i);
        }
    }
}
