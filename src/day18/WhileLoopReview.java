package day18;

public class WhileLoopReview {
    public static void main(String[] args) {
        int x=5;
        while(x>=1){
            System.out.print("$");//$$$$$
            --x;
        }
        System.out.println();
        int y=4;
        while(y>=1){
            System.out.print(y);
            --y;
        }

    }
}
